// program to convert the distance of kilometers to miles

public class DistanceConversion{
	
	public static void main (String[] args){
		
		// Take Distance in Kilometers
		double kilometers = 10.8;
		
		// Convert the distance of kilometer to miles
		double miles = kilometers * 1.6;
		
		// Print the distance of kilometer to miles
		System.out.println("The Distance " + kilometers + "km in miles is : " + miles + " miles ");
		
	}
	
}
			